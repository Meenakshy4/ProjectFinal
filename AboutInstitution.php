            <h1>About Marian College Kuttikkanam</h1>
            <p>Marian College Kuttikkanam Autonomous, affiliated to Mahatma Gandhi University Kottayam, Kerala, was founded in 1995 and is managed by the Catholic Diocese of Kanjirapally through the Peermade Development Society (PDS). The institution is the realization of the vision of the Management to provide quality higher education opportunities in the backward hill district of Idukki which has tribal, migrant, and minority populations.</p>
            <p>The College is nestled in the green highlands of the Western Ghats, where biodiversity is garbed in the mantle of a cool, misty, and salubrious ambience ideal for academic pursuits. With her focus on job-oriented study programs with an outcome-based, technology-integrated learning approach, Marian is committed to improving the educational standing of the district and preparing students to meet the challenges of the competitive job market and life situations. Banking on the divine direction of Christ Jesus and the benefaction of Mother Mary, the College champions her commitment to the ‘full-flowering of life in abundance’.</p>
            <p>The educational vision of the Management has given a sense of direction to the development of the College over the years.Marian performed consistently in the NAAC accreditations securing high scores. The College was first accredited in 2003 with B++ (83%) and again reaccredited with an ‘A’ grade (CGPA of 3.45) in 2009. During the third cycle of accreditation, held in 2014, Marian scored a high ‘A’ grade (CGPA 3.52/4). The College was conferred with the prestigious CPE status by UGC in
2009 and it was extended in 2014 making Marian eligible for phase II financial assistance. In 2016, the institution was elevated to the status of an Autonomous College by the UGC and was ranked 84th in NIRF 2018. Marian has mentored three colleges to get successful accreditation under the PARAMARSH scheme, including a college from North East India. Marian has a glorious tradition of academic excellence proven through University ranks and high placements. The College has academic tie-ups with several renowned international Universities like Hong Kong Baptist University, Chinese University of
Hong Kong, Lincoln University College Malaysia, etc. Marian imparts information with the intention of the right character formation for the students, eventually achieving their holistic transformation. As a premier center of knowledge generation and empowerment, Marian has always been at the forefront to take up new academic and co-academic initiatives</p>
